/*
    显示出读取到的三个整数的和
*/

#include <stdio.h>

int main(void){
    int n1,n2,n3;
    int sum;                                     //定义和变量

    puts("请输入三个整数。"); 
    printf("整数1：");  scanf("%d",&n1);
    printf("整数2：");  scanf("%d",&n2);
    printf("整数3：");  scanf("%d",&n3);        //读取输入的整数值

    sum = n1 + n2 + n3;

    printf("它们的和是%d。\n",sum);              //显示和
    
    return(0);
}
